const now = Date.now()
const HOUR = 3600_000
const DAY = 86400_000

const at = (offsetMs: number) => new Date(now + offsetMs).toISOString()
// today at a specific hour:minute
function todayAt(h: number, m = 0) {
  const d = new Date()
  d.setHours(h, m, 0, 0)
  return d.toISOString()
}
function dayAt(dayOffset: number, h: number, m = 0) {
  const d = new Date(now + dayOffset * DAY)
  d.setHours(h, m, 0, 0)
  return d.toISOString()
}

